    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String mid = txtid.getText();
        Item bitem= (Item) txtbook.getSelectedItem();
        SimpleDateFormat date_format = new  SimpleDateFormat("yyyy-MM-dd");
        String issuedate=date_format.format(txtdate.getDate());
        
         SimpleDateFormat date_format1 = new  SimpleDateFormat("yyyy-MM-dd");
        String returndate=date_format1.format(txtreturn.getDate());

        try {
            pst = con.prepareStatement("insert into borrow(memberid,bookid,issuedate,returndate)values(?,?,?,?)");
            pst.setString(1, mid);
            pst.setInt(2, bitem.id);
            pst.setString(3,issuedate);
            pst.setString(4,returndate);
 
            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Borrow Created");

                txtbook.setSelectedIndex(-1);
                txtname.setText("");
                txtid.setText("");
                txtid.requestFocus();

                Load();
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
       
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

